
public class point
{
	 private int x;
	 private int y;
		    
	public
		
	point()
	{
		x=0;
		y=0;
	}
	point(int x, int y)
	{
		this.x=x;
		this.y=y;
	}
	void setX(int a)
		{
		 	x=a;
		}
		void setY(int a)
		{
			y=a;
		}
		int getX()
		{
			return this.x;
		}
		int getY()
		{
			return this.y;
		}
}
